The sum of the primes below 10 is 2 + 3 + 5 + 7 = 17.
Find the sum of all the primes below two million.*/
- (void)sumOfPrimes {//142913828922
    
    //Sieve of Eratosthenes algorithm to find prime numbers
    int maxLimit              =   2000000;
    NSMutableArray *list    =   [NSMutableArray arrayWithNumbersInRange:NSMakeRange(0, maxLimit)];
    list[1] =   [NSNumber numberWithInt:0];
    
    int p   =   2;
    while (pow(p, 2) <= maxLimit) {
     
        int j   =   pow(p, 2);
        
        while (j <= maxLimit) {
            
            list[j] =   [NSNumber numberWithInt:0];//replace non-prime numbers with '0'
            j       +=  p;
        }
        if ([list[p] integerValue] !=  1)
            p   ++;
    }
    NSLog(@"result %@", [list valueForKeyPath:@"@sum.self"]);
}

